#include<iostream>
using namespace std;

int main() {
    int a = 12;
    float b = 2.36f;  // We use f/ F to express the float value 
    char c ='S';
    double d = 23.56l ;  // We use l/ L to express the long double value
    bool e = true ;
    string s = "Sandip" ;


    cout<<a<<endl<<b<<endl<<c<<endl<<d<<endl<<e<<endl<<s<<endl;
    return 0;
}