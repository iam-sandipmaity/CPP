
// Increament And Decrement oparetor 

#include<iostream>
using namespace std;

int main() {
    int a = 5;
    cout<<"The Value Of a Is  : "<<a<<endl;       //5   
    cout<<"The Value Of a++ Is  : "<<a++<<endl;   //5   
    cout<<"The Value Of a-- Is  : "<<a--<<endl;   //6  
    cout<<"The Value Of ++a Is  : "<<++a<<endl;   //6     
    cout<<"The Value Of --a Is  : "<<--a<<endl;   //5    
    
    return 0;
}